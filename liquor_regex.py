import regex as mrab

low_hanging_regex = mrab.compile(r"""(?sxm)
# First line
(^[^\r\n]*)[\r\n]+
# (1)
(\(1\)(?:(?!\(1).)*?(?=\(2))
\s*
# (2)
(\(2\)(?:(?!\(2).)*?(?=\(3))
\s*
# (3)
(\(3\)(?:(?!\(3).)*?(?=\(4))
\s*
# (4)
(\(4\)(?:(?!\(4).)*?(?=\(5))
\s*
# (5)
(\(5\)(?:(?!\(5).)*?(?=[\r\n]+^$))
""")

